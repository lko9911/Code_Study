#include <stdio.h>

// 1. 정적 배열 
int a[10];

/*
특징

1. 스택(Stack) 메모리에 할당
2. 컴파일 시 또는 함수 호출 시 크기 고정
3. 함수가 종료되면 자동으로 메모리 해제
4. 접근 속도가 빠름

크기를 런타임에 바꾸기 어려움
*/

// 2. 동적 배열 (C++에서는 std::vector 권장)
int n;
scanf("%d", &n);
int a[n];   // ❌ C99에서는 가능하지만, C++에서는 불가

// 3. C 언어의 동적 배열 (Heap 영역)
int* a = (int*)malloc(n * sizeof(int));

/*
특징

1. 힙(Heap) 메모리에 할당
2. 런타임에 크기 결정 가능
3. 함수가 끝나도 메모리 유지됨
4. 반드시 직접 해제해야 함

주의사항
malloc은 초기화하지 않음
메모리 해제를 하지 않으면 메모리 누수 발생

*/

// 메모리 해제
free(a);

